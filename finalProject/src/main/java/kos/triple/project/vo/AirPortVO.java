package kos.triple.project.vo;

public class AirPortVO {
	
	private String airportNo;
	private String airportName;
	private String airportTel;
	private double xPos;
	private double yPos;
	
	public AirPortVO() {
		
	}

	public String getAirportNo() {
		return airportNo;
	}

	public void setAirportNo(String airportNo) {
		this.airportNo = airportNo;
	}

	public String getAirportName() {
		return airportName;
	}

	public void setAirportName(String airportName) {
		this.airportName = airportName;
	}

	public String getAirportTel() {
		return airportTel;
	}

	public void setAirportTel(String airportTel) {
		this.airportTel = airportTel;
	}

	public double getxPos() {
		return xPos;
	}

	public void setxPos(double xPos) {
		this.xPos = xPos;
	}

	public double getyPos() {
		return yPos;
	}

	public void setyPos(double yPos) {
		this.yPos = yPos;
	}
	
	
	
}
