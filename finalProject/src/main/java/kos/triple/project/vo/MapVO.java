package kos.triple.project.vo;

public class MapVO {
	
	private String key;
	private Object value;
	
	public MapVO() { }
	
	public String getKey() {
		return key;
	}
	public void setKey(String key) {
		this.key = key;
	}
	public Object getValue() {
		return value;
	}
	public void setValue(Object value) {
		this.value = value;
	}
	
	
}
